    public void loaddata(User us){
        String Jabatan = "";
        DefaultTableModel t = new DefaultTableModel();
        t.addColumn("Username");
        t.addColumn("Password");
        t.addColumn("Email");
        t.addColumn("Level");
        bukuTable.setModel(t);
        
        try {
            while (us.r.next()) {
                try {
                    if("1".equals(us.r.getString("level"))){
                       Jabatan = "User"; 
                    }
                    else{
                       Jabatan = "Admin";
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(Datauser.class.getName()).log(Level.SEVERE, null, ex);
                }
                t.addRow(new Object[] {
                    us.r.getString("username"),
                    us.r.getString("pass"),
                    us.r.getString("email"),
                    Jabatan
                });
            }
            us.r.close();
        } catch (Exception e) {
             javax.swing.JOptionPane.showMessageDialog(rootPane, e);
        }
    }
    
    private boolean hasPeminjaman(String username) throws SQLException {
        Peminjaman pem = new Peminjaman();
        pem.setUsername(username);
        pem.r = pem.getPeminjamanUsername();
        if(pem.r.next()){
            return true;
        }
        else{
            return false;
        }
    }
    
