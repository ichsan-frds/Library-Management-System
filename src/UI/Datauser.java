    private void hapusButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusButtonActionPerformed
        // TODO add your handling code here:
        editButton.setEnabled(false);
        saveButton.setEnabled(false);
        searchButton.setEnabled(false);
        usernameField.setEnabled(true);
        if (usernameField.getText().isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(null, "Harap isi semua kolom yang diperlukan");
            usernameField.requestFocus();
            return;
        }  
        else
        {
            String username = usernameField.getText();
            try {
                if (hasPeminjaman(username)) {
                    javax.swing.JOptionPane.showMessageDialog(null, "Tidak dapat menghapus user karena user sedang meminjam");
                    return;
                }
                else{
                    User us = new User();
                    us.setUsernamebaru(usernameField.getText());
                    us.r = us.getUserUsername();
                    
                    if (!us.r.next()) {
                        javax.swing.JOptionPane.showMessageDialog(null, "Data User tidak ditemukan");
                        usernameField.setText(null);
                        usernameField.requestFocus();
                        return; 
                    }
                    
                    us.deleteUser();

                    javax.swing.JOptionPane.showMessageDialog(null, "Delete Success");
                    
                    us.r = us.getUser();
                    loaddata(us);

                    usernameField.setEnabled(false);
                }
            } catch (SQLException ex) {
                Logger.getLogger(Datauser.class.getName()).log(Level.SEVERE, null, ex);
            }
        usernameField.setText(null);
        saveButton.setEnabled(true);
        searchButton.setEnabled(true);
        }
    }//GEN-LAST:event_hapusButtonActionPerformed

