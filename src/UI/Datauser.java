    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        saveButton.setEnabled(false);
        hapusButton.setEnabled(false);
        usernameField.setEnabled(true);
        if(usernameField.getText().isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(null, "Isi Kolom Username");
            usernameField.requestFocus();
            return;
        }
        else{
            User us = new User();
            us.setUsernamebaru(usernameField.getText());
            try{
                us.r = us.getUserUsername();
                
                if (!us.r.next()) {
                        javax.swing.JOptionPane.showMessageDialog(null, "Data User tidak ditemukan");
                        usernameField.setText(null);
                        usernameField.requestFocus();
                        return; 
                    }
                
                us.r = us.getUserUsername();
                while(us.r.next()){
                    usernamelamaLabel.setText(usernameField.getText());
                    passwordField.setText(us.r.getString("pass"));
                    emailField.setText(us.r.getString("email"));
                    levelComboBox.setSelectedItem(us.r.getString("level"));
                    javax.swing.JOptionPane.showMessageDialog(null, "Search Success");
                }
                us.r =  us.getUserUsername();
                loaddata(us);
                editButton.setEnabled(true);
                saveButton.setEnabled(false);
                hapusButton.setEnabled(false);
                searchButton.setEnabled(false);
                passwordField.setEnabled(true);
                levelComboBox.setEnabled(true);
                emailField.setEnabled(true);
            } catch(Exception e){
                javax.swing.JOptionPane.showMessageDialog(null, "Error: "+e.getMessage());
            }
        }
    }//GEN-LAST:event_searchButtonActionPerformed

