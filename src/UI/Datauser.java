    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        usernameField.setEnabled(true);
        passwordField.setEnabled(true);
        levelComboBox.setEnabled(true);
        emailField.setEnabled(true);
        editButton.setEnabled(false);
        hapusButton.setEnabled(false);
        searchButton.setEnabled(false);
        if (usernameField.getText().isEmpty() || passwordField.getText().isEmpty() || emailField.getText().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(null, "Isi semua kolom yang diperlukan");
            return;
        }
        else{
            User us = new User();
            us.setUsernamebaru(usernameField.getText());
            us.r = us.getUserUsername();
            try {
                if (us.r.next()) {
                    javax.swing.JOptionPane.showMessageDialog(null, "Terdapat Duplikat Data (username)");
                    usernameField.setText(null);
                    usernameField.requestFocus();
                    return;
                } 
            } catch (SQLException ex) {
                Logger.getLogger(Datauser.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            us.setUsernamebaru(usernameField.getText());
            us.setPass(passwordField.getText());
            us.setEmail(emailField.getText());
            us.setLevel((String) levelComboBox.getSelectedItem());
            
            us.insertUser();
            us.r = us.getUser();
            loaddata(us);
            
            javax.swing.JOptionPane.showMessageDialog(null, "Save Success");
        }
        usernameField.setText(null);
        passwordField.setText(null);
        levelComboBox.setSelectedIndex(0);
        emailField.setText(null);
        usernameField.setEnabled(false);
        passwordField.setEnabled(false);
        levelComboBox.setEnabled(false);
        emailField.setEnabled(false);
        hapusButton.setEnabled(true);
        searchButton.setEnabled(true);
    }//GEN-LAST:event_saveButtonActionPerformed

