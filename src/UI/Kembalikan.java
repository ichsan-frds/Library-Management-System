    public void loaddata(Peminjaman pem){
        Buku buk = new Buku();
        DefaultTableModel t = new DefaultTableModel();
        t.addColumn("ID Transaksi");
        t.addColumn("Judul");
        t.addColumn("Tgl Pinjam");
        t.addColumn("Tgl Balik");
        t.addColumn("Status");
        transaksiTable.setModel(t);
        try {
            while (pem.r.next()) {
                buk.setId(pem.r.getString("id_buku"));
                buk.r = buk.getBukuId();
                if (buk.r.next() && "sedang dipinjam".equals(pem.r.getString("status_peminjaman"))) {
                    t.addRow(new Object[] {
                        pem.r.getString("id_transaksi"),
                        buk.r.getString("judul"),
                        pem.r.getString("tgl_pinjam"),
                        pem.r.getString("tgl_balik"),
                        pem.r.getString("status_peminjaman")
                    });
                }
                buk.r.close();
            }
            pem.r.close();
        } catch (Exception e) {
             javax.swing.JOptionPane.showMessageDialog(rootPane, e);
        }
    }
    
    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        User us = new User();
        us.setUsernamebaru(usernameField.getText());
        us.r = us.getUser();
        
        try {
            if (!us.r.next()) {
                javax.swing.JOptionPane.showMessageDialog(null, "Data User tidak ditemukan");
                usernameField.setText(null);
                usernameField.requestFocus();
                return;
            }
        } catch (SQLException ex) {
            Logger.getLogger(Kembalikan.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        Peminjaman pem = new Peminjaman();
        pem.setUsername(usernameField.getText());
        pem.r = pem.getPeminjamanUsername();
        javax.swing.JOptionPane.showMessageDialog(null, "Transaksi Ditemukan");
        loaddata(pem);
        kembalikanButton.setEnabled(true);
        usernameField.setEnabled(false);
        idtransaksiField.setEnabled(true);
    }//GEN-LAST:event_searchButtonActionPerformed

