    private void kembalikanButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kembalikanButtonActionPerformed
        // TODO add your handling code here:
        if (idtransaksiField.getText().isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(null, "Harap isi semua kolom yang diperlukan");
        }
        else{
            Peminjaman pem = new Peminjaman();
            pem.setTransaksi(idtransaksiField.getText());
            pem.r = pem.getPeminjamanId();
            
            try {
                if (!pem.r.next()) {
                    javax.swing.JOptionPane.showMessageDialog(null, "ID Transaksi tidak ditemukan");
                    idtransaksiField.setText(null);
                    idtransaksiField.requestFocus(); 
                    return;
                }
            } catch (SQLException ex) {
                Logger.getLogger(Kembalikan.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            pem.setStatus("sudah dikembalikan");
            pem.updatePeminjaman();
            javax.swing.JOptionPane.showMessageDialog(null, "Pengembalian Berhasil");
            
            pem.setUsername(usernameField.getText());
            pem.r = pem.getPeminjamanUsername();
            loaddata(pem);

            usernameField.setText(null);
            idtransaksiField.setText(null);
            idtransaksiField.setEnabled(false);
            kembalikanButton.setEnabled(false);
            usernameField.setEnabled(true);
            usernameField.requestFocus();
        }
    }//GEN-LAST:event_kembalikanButtonActionPerformed

