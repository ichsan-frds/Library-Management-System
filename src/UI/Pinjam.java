    private void loaddata() {
        Buku buk = new Buku();
        int i = 0;
        try{
            buk.r = buk.getBuku();
            while(buk.r.next()){
                bukuTable.setValueAt(buk.r.getString("id_buku"), i, 0) ;
                bukuTable.setValueAt(buk.r.getString("judul"), i, 1) ;
                bukuTable.setValueAt(buk.r.getString("jenis") , i, 2);
                bukuTable.setValueAt(buk.r.getString("tahun_terbit"), i, 3);
                i++;
            }
            buk.r.close();
        } catch(Exception e){
            javax.swing.JOptionPane.showMessageDialog(null, "Error: "+e.getMessage());
        }
    }

