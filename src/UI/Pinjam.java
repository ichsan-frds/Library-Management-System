    private void pinjamButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pinjamButtonActionPerformed
        // TODO add your handling code here:
        if (idtransaksiField.getText().isEmpty() || idbukuField.getText().isEmpty() || tglpinjamDate.getDate() == null || tglkembaliDate.getDate() == null){
            javax.swing.JOptionPane.showMessageDialog(null, "Harap isi semua kolom yang diperlukan");
        }
        else{
            User us = new User();
            us.setUsernamebaru(usernameField.getText());
            Peminjaman pem = new Peminjaman();
            pem.setTransaksi(idtransaksiField.getText());
            Buku buk = new Buku();
            buk.setId(idbukuField.getText());
            
            us.r = us.getUserUsername();
            pem.r = pem.getPeminjamanId();
            buk.r = buk.getBukuId();
            
            try {
                if (!us.r.next()) {
                    javax.swing.JOptionPane.showMessageDialog(null, "Data User tidak ditemukan");
                    usernameField.setText(null);
                    usernameField.requestFocus(); 
                    return;
                }
            } catch (SQLException ex) {
                Logger.getLogger(Pinjam.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            try {
                if (pem.r.next()) {
                    javax.swing.JOptionPane.showMessageDialog(null, "Terdapat Duplikat Data (id_transaksi)");
                    idtransaksiField.setText(null);
                    idtransaksiField.requestFocus();
                    return;
                }
            } catch (SQLException ex) {
                Logger.getLogger(Pinjam.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            try {
                if (!buk.r.next()) {
                    javax.swing.JOptionPane.showMessageDialog(null, "Data Buku tidak ditemukan");
                    idbukuField.setText(null);
                    idbukuField.requestFocus(); 
                    return;
                }
            } catch (SQLException ex) {
                Logger.getLogger(Pinjam.class.getName()).log(Level.SEVERE, null, ex);
            }

            Date pinjam = tglpinjamDate.getDate();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String formattedDatepinjam = sdf.format(pinjam);
            Date kembali = tglkembaliDate.getDate();
            String formattedDatekembali = sdf.format(kembali);
            
            Calendar calPinjam = Calendar.getInstance();
            calPinjam.setTime(tglpinjamDate.getDate());

            Calendar calKembali = Calendar.getInstance();
            calKembali.setTime(tglkembaliDate.getDate());

            if (calKembali.before(calPinjam)) {
                javax.swing.JOptionPane.showMessageDialog(null, "Tanggal Kembali < Tanggal Pinjam");
                tglpinjamDate.setDate(null);
                tglkembaliDate.setDate(null);
                tglpinjamDate.requestFocus();
                return;
            } 

            pem.setTransaksi(idtransaksiField.getText());
            pem.setTglpinjam(formattedDatepinjam);
            pem.setTglkembali(formattedDatekembali);
            pem.setStatus("sedang dipinjam");
            pem.setBuku(idbukuField.getText());
            pem.setUsername(usernameField.getText());
            pem.insertPeminjaman();
            javax.swing.JOptionPane.showMessageDialog(null, "Pinjam Success");

            usernameField.setText(null);
            idtransaksiField.setText(null);
            idbukuField.setText(null);
            tglpinjamDate.setDate(null);
            tglkembaliDate.setDate(null);
            usernameField.requestFocus();
        }
    }//GEN-LAST:event_pinjamButtonActionPerformed

